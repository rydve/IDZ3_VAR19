import numpy as np

# Данные выборки
data = [-10.37, -9.52, -6.71, -6.30, -6.03, -5.95, -5.72, -5.36, -5.25, -5.24,
        -4.05, -4.00, -3.94, -3.63, -3.45, -3.38, -2.78, -2.73, -2.53, -2.16,
        -1.94, -1.75, -1.67, -1.59, -0.30, 0.05, 0.15, 0.34, 0.35, 0.63,
        1.00, 1.82, 2.78, 2.86, 3.26, 3.85, 4.12, 4.63, 4.67, 4.74, 4.90,
        5.04, 5.08, 5.44, 6.37, 6.84, 7.60, 8.13, 9.67, 11.25]

# Количество элементов
n = len(data)

# Среднее выборки
mean_x = np.mean(data)

# Несмещенная дисперсия (по формуле)
variance_unbiased = np.sum((data - mean_x) ** 2) / (n - 1)

# Стандартное отклонение (СКО)
std_dev_unbiased = np.sqrt(variance_unbiased)

# Проверка с numpy
variance_numpy = np.var(data, ddof=1)
std_dev_numpy = np.std(data, ddof=1)

# Вывод результатов
print(f"Несмещенная дисперсия (по формуле): {variance_unbiased:.4f}")
print(f"Несмещенная дисперсия (numpy): {variance_numpy:.4f}")
print(f"Несмещенное СКО (по формуле): {std_dev_unbiased:.4f}")
print(f"Несмещенное СКО (numpy): {std_dev_numpy:.4f}")
